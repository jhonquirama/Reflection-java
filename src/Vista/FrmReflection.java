    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        String nombre = null;

        try {
            nombre = this.txtPaquete.getText().trim();
            if (nombre.isEmpty() || nombre == null) {
                System.out.println("ingrese el nombre del paquete");

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ingrese por favor el nombre del paquete");
        }

        DefaultComboBoxModel cb = this.refle.cargar(nombre);

        if (cb == null) {
            cb = new DefaultComboBoxModel();

            cb.addElement("seleccione las clases");

            this.LimpiarCb();

        }
        this.cbClases.setModel(cb);

    }//GEN-LAST:event_btnCargarActionPerformed

    private void cbClasesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbClasesItemStateChanged
        String nombre = null;

        try {
            nombre = this.cbClases.getSelectedItem().toString();
            if (nombre.isEmpty() || nombre == null) {
                System.out.println("se da√±o");

            } else if (this.cbClases.getSelectedIndex() == 0) {
                this.LimpiarCb();
                return;
            }
        } catch (Exception e) {
            System.out.println("pailas");
        }

        this.cbAtributos.setModel(this.refle.cargarLosAtributos(nombre));
        this.cbMetodos.setModel(this.refle.cargarLosMetodos(nombre));

    }//GEN-LAST:event_cbClasesItemStateChanged

